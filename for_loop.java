 class ForL
 {
   /// ******** FOR LOOP ************* ////
   public static void main(String args[])
   { 
     
     for(int i = 0; i < 10; i++){    /// begining Of for loop  ( initialize; condition; increament or decreament)
    System.out.println(i);   /// print value of i
    }
    
     for(int j = 0; j < 20; j = j + 5){  /// begining Of for loop  ( initialize; condition; increament or decreament)
    System.out.println(j);   //// print value of j and its increament with 5
    }
    
    
    
   }
 }
