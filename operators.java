 class Operators
 {
   public static void main(String args[])
   { 
     int a = 5;     // a variable declare with integer data type assign value 5
     int b = 5;     // b variable declare with integer data type assign value 5
     
        
 
     
     /// ARITHMETIC OPERATORS  - + , - , * , / , %
 
       System.out.print("ARITHMETIC OPERATOR ");    
      System.out.print(" A = 5 and B = 5  ");
    
    System.out.print("\n (+)  ");
    System.out.println(a + b);
    
    System.out.print(" (-)  ");
    System.out.println(a - b);
    
    System.out.print(" (*)  ");
    System.out.println(a * b);
    
    System.out.print(" (/)  ");
    System.out.println(a / b);
    
    System.out.print(" (%)  ");
    System.out.println(a % b);
    
    
    /// LOGICAL OPERATOR  - AND Operator : && , OR Operator : || , NOT Operator :  !
    
    System.out.print("\n \n \nLOGICAL OPERATOR ");  
    boolean c = true;
    boolean d = false;
    
    System.out.println("\n AND Operator");
    
    
    System.out.println(" C = true AND D = false ");
    
    
    System.out.println(c && c);   //// when both conditions are true then statement are true is called AND operator
    
    
    System.out.println("\n OR Operator");
    
    
    System.out.println(" C = true AND D = false ");
    
    
    System.out.println(c && d);   //// when one condition are true then statement are true is called OR operator
    
    
    
    System.out.println("\n NOT Operator");
    
    
    System.out.println(" C = true AND D = false ");
    
    
    System.out.println(c != d);   //// when one condition are true then statement are true is called OR operator
    
    
    /// COMPARISON OPERATOR - Greater than : > , Less than : < , Greater then or equal to : >= , less then or equal to : <= , equal to : ==
    System.out.print(" \n \n \n COMPARISON OPERATOR ");  
    System.out.println("\n A = 5 and B = 5  ");
 
 
    System.out.println("\n Greater than A > B");
    
    System.out.println(a > b);
    
    
    System.out.println("\n Less than A < B");
    
    System.out.println(a < b);
    
    System.out.println("\n Greater than  or equal to A >= B");
    
    System.out.println(a >= b);
    
    
    System.out.println("\n Less than  or equal to A <= B");
    
    System.out.println(a <= b);
    
    System.out.println("\n EQUAL TO A == B");
    
    System.out.println(a == b);
    
    
    
    
    
    
   }
 }
