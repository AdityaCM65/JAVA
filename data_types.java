import java.util.*;

 // Compiler version JDK 11.0.2

 class Dtypes // this is Class with name Dtypes
 {
   public static void main(String args[])
   {
     
     int a = 5;      // a is variable and its data type is integer assign value is 5  
     double b = 6.7;    // b is variable and its data type is double assign value is 6.7
     boolean c = true;   // c is variable and its data type is boolean assign value is true
     String d = "aditya";    // d is variable and its data type is string assign value is aditya
     char e = 'A';          // e is variable and its data type is char assign value is A
     
     
     
     System.out.println("************ DATA TYPES IN JAVA *************");
      
    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
    
    
    
   }
 }
